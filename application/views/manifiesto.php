              <h2>¿Qué hacemos?</h2>
              <p>
                Sansanos por el Software Libre es una iniciativa de <abbr class="initialism" title="Computer Systems Research Group">CSRG</abbr> orientada al desarrollo y difusión del Software Libre. Sus principales objetivos son:
              </p>
              <ul>
                <li>Difundir e incentivar el uso del Software Libre en sus distintos ámbitos, tanto en el área de la Informática como en las distintas disciplinas que interactúan de manera directa o indirecta con la tecnología y la sociedad de la información en general.</li>
                <li>Participar y contribuir en proyectos de gran escala, involucrándose directamente en desarrollo de software de alta exigencia y estándares internacionales.</li>
                <li>Contribuir a la comunidad local con proyectos de pequeña y mediana envergadura, los cuales puedan ser replicados a nivel regional y/o nacional.</li>
              </ul>
              <p>
                Para ello, se busca cumplir con los siguientes objetivos secundarios:
              </p>
              <ul>
                <li>Participar en las principales actividades orientadas a difundir el Software Libre, además de relatar experiencias relacionadas a él (e.g. <abbr class="initialism" title="Festival Latinoamericano de Instalación de Software Libre">FLISOL</abbr>, Encuentro Linux).</li>
                <li>Desarrollar actividades que permitan difundir e incentivar el uso de Software Libre a nivel local (e.g. Installfest).</li>
                <li>Participar constantemente en grandes proyectos de Software Libre, ya sea en desarrollo, difusión, traducción, etc.</li>
                <li>Participar, como equipo <abbr title="Sansanos por el Software Libre">ssl</abbr>, en la <abbr class="initialism" title="Google Summer of Code">GSoC</abbr>.</li>
                <li>Identificar problemáticas de la comunidad local y resolverlas a través de la implementación de soluciones basadas en Software Libre.</li>
              </ul>
            </div>
            <div class="span5">
              <h2>Misión</h2>
              <p>Inculcar los valores del Software Libre entre sus miembros y difundirlos desde y hacia la comunidad, haciendo uso de las prácticas que de ello derivan, a saber: desarrollo de habilidades blandas, perfeccionamiento de la técnica, trabajo en equipo e interacción multidisciplinaria y multicultural.</p>
              <h2>Visión</h2>
              <p>Involucrar al futuro Ingeniero en la filosofía del Software Libre, haciéndolo participe de la generación constante de conocimiento nuevo y libre, contribuyendo además al desarrollo de la sociedad de la cual forma parte. En pocas palabras: Free software for Free people.</p>
